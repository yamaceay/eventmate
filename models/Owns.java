public class Owns {

	private int userId;

	private int placeId;

	public int getUserId() {
		return userId;
	}

	public int getPlaceId() {
		return placeId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public void setPlaceId(int placeId) {
		this.placeId = placeId;
	}

	public Owns(int userId, int placeId) {
		this.userId = userId;
		this.placeId = placeId;
	}
	
	

}
